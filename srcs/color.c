#include "color.h"

t_uint32	t_colortouint32(t_color c)
{
	t_uint32	u;

	u = c.a;
	u <<= 8;
	u += c.r;
	u <<= 8;
	u += c.g;
	u <<= 8;
	u += c.b;
	return (u);
}
